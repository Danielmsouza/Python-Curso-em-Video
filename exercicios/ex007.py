'''Program that reads the 2 notes and calculates the average '''
'''Programa que le as 2 notas e calcula a media'''

note1 = float(input('Enter the first note: '))
note2 = float(input('Enter the second note: '))
average = (note1 + note2) / 2

print(f'The average of the grades {note1} and {note2} is {average}')
